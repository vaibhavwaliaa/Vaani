  /**
   * Replace complex words with simpler alternatives
   */
  private replaceComplexWords(text: string, simplifications: Record<string, string>): string {
    let result = text;
    
    // Sort by length (longest first) to handle compound words better
    const sortedSimplifications = Object.entries(simplifications)
      .sort((a, b) => b[0].length - a[0].length);
    
    sortedSimplifications.forEach(([complex, simple]) => {
      const regex = new RegExp(`\\b${this.escapeRegex(complex)}\\b`, 'gi');
      result = result.replace(regex, (match) => {
        // Preserve capitalization
        if (match[0] === match[0].toUpperCase() && match.length > 1) {
          return simple.charAt(0).toUpperCase() + simple.slice(1);
        }
        return simple;
      });
    });
    
    return result;
  }

  /**
   * Break long sentences into shorter ones (works for all languages)
   */
  private breakLongSentences(text: string, maxWords: number): string {
    // Split by sentence endings
    const sentences = text.split(/(?<=[.!?редрее])\s+/);
    const simplified: string[] = [];
    
    sentences.forEach((sentence) => {
      const words = sentence.trim().split(/\s+/);
      
      if (words.length <= maxWords) {
        simplified.push(sentence);
      } else {
        // Break at punctuation marks
        const parts = this.splitLongSentence(sentence, maxWords);
        simplified.push(...parts);
      }
    });
    
    return simplified.join(' ');
  }

  /**
   * Split a long sentence at natural pause points
   */
  private splitLongSentence(sentence: string, maxWords: number): string[] {
    const parts: string[] = [];
    const words = sentence.split(/\s+/);
    let currentPart: string[] = [];
    
    words.forEach((word) => {
      currentPart.push(word);
      
      // Break at commas or conjunctions
      const hasComma = word.includes(',');
      const isConjunction = ['рдФрд░', 'ржПржмржВ', 'р░ор░░р░┐р░пр▒Б', 'рооро▒рпНро▒рпБроорпН', 'р▓ор▓др│Нр▓др│Б', 'рдФрд░', 'ркЕркирлЗ', 'р┤Хр╡Вр┤Яр┤╛р┤др╡Ж', 'and', 'but', 'or'].includes(word.toLowerCase());
      
      if ((hasComma || isConjunction) && currentPart.length >= 5) {
        parts.push(currentPart.join(' ').replace(/,\s*$/, ''));
        currentPart = [];
      } else if (currentPart.length >= maxWords) {
        parts.push(currentPart.join(' '));
        currentPart = [];
      }
    });
    
    if (currentPart.length > 0) {
      parts.push(currentPart.join(' '));
    }
    
    return parts.map(p => p.trim()).filter(p => p.length > 0);
  }

  /**
   * Add emojis for better comprehension (all languages)
   */
  private addEmojis(text: string): string {
    let result = text;
    
    Object.entries(this.universalEmojiMap).forEach(([word, emoji]) => {
      const regex = new RegExp(`\\b${this.escapeRegex(word)}\\b`, 'gi');
      result = result.replace(regex, `${word} ${emoji}`);
    });
    
    return result;
  }

  /**
   * Clean up formatting
   */
  private cleanupFormatting(text: string): string {
    return text
      .replace(/\s+/g, ' ')
      .replace(/\s+([.,!?редрее])/g, '$1')
      .replace(/([.,!?редрее])([A-Za-z╨Р-╨п╨░-╤ПсГР-сГ░\u0900-\u097F\u0980-\u09FF\u0A00-\u0A7F\u0A80-\u0AFF\u0B00-\u0B7F\u0B80-\u0BFF\u0C00-\u0C7F\u0C80-\u0CFF\u0D00-\u0D7F])/g, '$1 $2')
      .trim();
  }

  /**
   * Escape special regex characters
   */
  private escapeRegex(str: string): string {
    return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  }

  /**
   * Get readability score (Flesch Reading Ease - adapted for multi-language)
   */
  getReadabilityScore(text: string): number {
    const sentences = text.split(/[.!?редрее]+/).filter(s => s.trim().length > 0);
    const words = text.split(/\s+/).filter(w => w.length > 0);
    
    if (sentences.length === 0 || words.length === 0) return 0;
    
    const avgWordsPerSentence = words.length / sentences.length;
    const avgWordLength = words.reduce((sum, word) => sum + word.length, 0) / words.length;
    
    // Modified formula for multi-language support
    const score = 206.835 - 1.015 * avgWordsPerSentence - 10 * avgWordLength;
    
    return Math.max(0, Math.min(100, score));
  }

  /**
   * Get reading level description
   */
  getReadingLevel(score: number): string {
    if (score >= 90) return 'Very Easy (Grade 5)';
    if (score >= 80) return 'Easy (Grade 6)';
    if (score >= 70) return 'Fairly Easy (Grade 7)';
    if (score >= 60) return 'Standard (Grade 8-9)';
    if (score >= 50) return 'Fairly Difficult (Grade 10-12)';
    return 'Difficult (College level)';
  }
